decisionBodyCode2decisionBodyId <- function(decisionBodyCodeInput) {
  fetch_multiple_decisionbody_list() |>
    dplyr::filter(
      decisionBodyId ==
        get_data_decisionbodyid_from_decisionbody_code(decisionBodyCodeInput)
    ) |>
    dplyr::pull(decisionBodyName)
}

